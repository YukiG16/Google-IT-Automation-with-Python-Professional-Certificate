import csv
import datetime

def categorize_employees(filename):
  """Categorizes employees based on their department, considering only those with 90+ days of tenure.

  Args:
    filename: The name of the CSV file containing employee data.
  """

  categorized_employees = {}
  with open('/content/ExampleEmployeeDataHireDate.CSV', 'r') as csvfile:
    reader = csv.DictReader(csvfile)
    for row in reader:
      hire_date = datetime.datetime.strptime(row['HireDate'], '%Y-%m-%d')
      tenure_days = (datetime.datetime.now() - hire_date).days
      if tenure_days >= 90:
        department = row['Department']
        if department not in categorized_employees:
          categorized_employees[department] = []
        categorized_employees[department].append(row['Name'] + ' (' + row['Email'] + ')')

  return categorized_employees

def print_results(categorized_employees):
  """Prints a summary of the categorized employees.

  Args:
    categorized_employees: A dictionary containing categorized employees.
  """

  for department, employees in categorized_employees.items():
    print(department + ':')
    for employee in employees:
      print('  - ' + employee)

if __name__ == '__main__':
  filename = 'employees.csv'
  categorized_employees = categorize_employees(filename)
  print_results(categorized_employees)

